function createRoundedCubeGeometry(THREE, size = 2.1, radius = 0.24, depth = 1.9) {
  const inset = size / 2 - radius
  const shape = new THREE.Shape()

  shape.moveTo(-inset, -size / 2)
  shape.lineTo(inset, -size / 2)
  shape.quadraticCurveTo(size / 2, -size / 2, size / 2, -inset)
  shape.lineTo(size / 2, inset)
  shape.quadraticCurveTo(size / 2, size / 2, inset, size / 2)
  shape.lineTo(-inset, size / 2)
  shape.quadraticCurveTo(-size / 2, size / 2, -size / 2, inset)
  shape.lineTo(-size / 2, -inset)
  shape.quadraticCurveTo(-size / 2, -size / 2, -inset, -size / 2)

  const geometry = new THREE.ExtrudeGeometry(shape, {
    depth,
    bevelEnabled: true,
    bevelSegments: 8,
    steps: 1,
    bevelSize: radius * 0.68,
    bevelThickness: radius * 0.68,
    curveSegments: 24,
  })

  geometry.center()

  return geometry
}

